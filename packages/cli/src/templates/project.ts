export function getReadmeTemplate(appName: string, opts: ProjectOptions): string {
  const pm = opts.pm || 'bun';
  const installCmd = pm === 'bun' ? 'bun install' : `${pm} install`;

  return `# ${appName}

Generated with Kanji Framework CLI.

## Quick Start

1. Install dependencies:
   \`\`\`bash
   ${installCmd}
   \`\`\`

2. Start the development server:
   \`\`\`bash
   ${pm} run dev
   \`\`\`

## Architecture & Features
${opts.db && opts.db !== 'none' ? `- **Database:** ${opts.db === 'postgres' ? 'PostgreSQL (Drizzle ORM)' : 'MongoDB (Native adapter)'}\n` : ''}${opts.auth && opts.auth.length > 0 ? `- **Authentication:** Active providers: ${opts.auth.join(', ')}\n` : ''}${opts.openapi ? '- **OpenAPI Documentation:** Enabled via OpenApiModule\n' : ''}${opts.tests ? '- **Testing:** Integration tests enabled\n' : ''}
`;
}

export function getAppE2ETestTemplate(): string {
  return `import { describe, it, expect, beforeAll } from 'bun:test';
import { KanjijsAdapter } from '@kanjijs/platform-hono';
import { AppModule } from '../app.module.js';
import type { Hono } from 'hono';

describe('AppController (E2E)', () => {
  let app: Hono;

  beforeAll(async () => {
    const adapter = await KanjijsAdapter.create(AppModule);
    app = adapter.app;
  });

  it('GET / should return welcome message', async () => {
    const res = await app.request('/');
    expect(res.status).toBe(200);
    const body = await res.json();
    expect(body.message).toBe('Welcome to Kanji Framework!');
  });
});
`;
}

export function getDbSchemaTemplate(): string {
  return `import { pgTable, uuid, varchar, timestamp } from 'drizzle-orm/pg-core';

export const users = pgTable('users', {
  id: uuid('id').defaultRandom().primaryKey(),
  email: varchar('email', { length: 255 }).notNull().unique(),
  name: varchar('name', { length: 255 }),
  createdAt: timestamp('created_at').defaultNow().notNull(),
});
`;
}

export function getDbSeedTemplate(): string {
  return `import { drizzle } from 'drizzle-orm/node-postgres';
import pg from 'pg';
import { users } from './schema/index.js';

const pool = new pg.Pool({
  connectionString: process.env.DATABASE_URL || 'postgres://postgres:postgres@localhost:5432/kanji_db',
});

const db = drizzle(pool);

async function main() {
  console.log('Seeding database...');
  await db.insert(users).values([
    {
      email: 'admin@kanji.dev',
      name: 'Admin User',
    },
  ]);
  console.log('Seed completed successfully!');
  await pool.end();
}

main().catch((err) => {
  console.error('Seed failed:', err);
  process.exit(1);
});
`;
}
