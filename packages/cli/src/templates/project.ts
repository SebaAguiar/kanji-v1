export function getAppModuleTemplate(opts: ProjectOptions): string {
  const imports: string[] = [
    "import { KanjijsModule } from '@kanjijs/core';",
    "import { AppController } from './app.controller.js';",
  ];

  const moduleImports: string[] = [];

  if (opts.db === 'postgres') {
    imports.push("import { StoreModule } from '@kanjijs/store';");
    moduleImports.push(`StoreModule.forRoot({
      type: 'postgres',
      connectionString:
        process.env.DATABASE_URL || 'postgres://postgres:postgres@localhost:5432/kanji_db',
    })`);
  } else if (opts.db === 'mongodb') {
    imports.push("import { StoreModule } from '@kanjijs/store';");
    moduleImports.push(`StoreModule.forRoot({
      type: 'mongodb',
      connectionString:
        process.env.DATABASE_URL || 'mongodb://root:password@localhost:27017/kanji_db?authSource=admin',
    })`);
  }

  if (opts.auth && opts.auth.length > 0) {
    imports.push("import { AuthModule } from '@kanjijs/auth';");
    moduleImports.push(`AuthModule.forRoot({
      jwtSecret: process.env.JWT_SECRET || 'dev-secret-key-12345',
    })`);
  }

  if (opts.openapi) {
    imports.push("import { OpenApiModule } from '@kanjijs/openapi';");
    moduleImports.push(`OpenApiModule.forRoot({
      title: '${opts.appName} API',
      version: '1.0.0',
      description: 'API generated by Kanji CLI',
    })`);
  }

  const importsStr = imports.join('\n');
  const moduleImportsStr = moduleImports.length > 0 
    ? `\n  imports: [\n    ${moduleImports.join(',\n    ')}\n  ],` 
    : '';

  return `${importsStr}

@KanjijsModule({${moduleImportsStr}
  controllers: [AppController],
  providers: [],
})
export class AppModule {}
`;
}

